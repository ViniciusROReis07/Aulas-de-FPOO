package com.java.heranca;

public class Animal {
	//*************************************Atributos******************************************************
	private String nome;
	private double peso;
	
	
	//**********************************Getters and Setters************************************************
	void setNome(String nome) {
		this.nome=nome;
	}
	String getNome() {
		return this.nome;
	}
	
	void setPeso(double peso) {
		this.peso=peso;
	}
	double getPeso() {
		return this.peso;
	}
	
	
	
}
